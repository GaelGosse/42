#include <unistd.h>

void	ft_putstr(char *str)
{
	int	i;
   
	i = 0;
	while(str[i] != '\0')
	{
		i++;
	}
	write(1, str, i);
}
int	main()
{
	char	*a;

	a = "hello bo ";
	ft_putstr(a);
}
