#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*result;
	int		i;

	i = 0;
	if (!src)
		return (NULL);
	result = (char *)malloc((ft_strlen(src) + 1) * sizeof(char));
	if (!result)
		return (NULL);
	while (src[i])
	{
		result[i] = src[i];
		i++;
	}
	result[i] = '\0';
	return (result);
}
